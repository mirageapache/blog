 //產生GUID
function generate_id()
{
	$newid = '';
	for($i=1;$i<=36;$i++){
		if($i == 9 || $i == 14 || $i == 19 || $i == 24){
			$newid = $newid."-";
		}
		else{
			$newid = $newid.dechex(rand(0,15)); //dechex()函數可以轉換為16進位
		}
	}
	return $newid;
}




